#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			num_chr;

	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	num_chr = n % 10 + '0';
	ft_putchar_fd(num_chr, fd);
}
